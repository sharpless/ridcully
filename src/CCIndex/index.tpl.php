<h1>Welcome to Ridcully</h1>
<p>Congratulations to your choice, now you need to set up this framework</p>

<h2>Download</h2>
<p>Get Ridcully from <a href="http://github.com/sharpless/ridcully">github</a> or by
using git:</p>
<code>git clone git://github.com/sharpless/ridcully.git</code>

<h2>Install</h2>
<p>Firstly, make sure that the data directory (<span class="code">ridcully/site/data</span>
is writable to the webserver. The easiest way to do this is by</p>
<code>cd ridcully; chmod 777 site/data</code>
<p>Secondly, some modules require initialisation. For now, click the link below.</p>
<p><a href="<?=create_uri('modules/install')?>">Install</a></p>